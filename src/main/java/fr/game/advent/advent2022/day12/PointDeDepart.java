package fr.game.advent.advent2022.day12;

public class PointDeDepart {
	private Integer x;
	private Integer y;
	
	public PointDeDepart(Integer x, Integer y) {
		this.x = x;
		this.y = y;
	}

	public Integer getX() {
		return x;
	}

	public void setX(Integer x) {
		this.x = x;
	}

	public Integer getY() {
		return y;
	}

	public void setY(Integer y) {
		this.y = y;
	}

}
